//FUNÇÃO ADIÇÃO:
function adicao (a,b) {
  resultado = a + b;
  return resultado;
}
console.log(adicao (10,5))

//FUNÇÃO SUBTRAÇÃO:
function subtracao (a,b) {
  resultado = a - b;
  return resultado;
}
console.log(subtracao (15,5))

//FUNÇÃO MULTIPLICAÇÃO:
function multiplicacao (a,b) {
  resultado = a * b;
  return resultado;
}
console.log(multiplicacao (15,5))

//FUNÇÃO DIVISÃO:
function divisao (a,b) {
  resultado = a / b;
  return resultado;
}
console.log(divisao (10,2))

//FUNÇÃO MÓDULO:
function modulo (a,b) {
  resultado = a % b;
  return resultado;
}
console.log(modulo (7,5))